CREATE TABLE `{question}` (
 `id` int(10) unsigned NOT NULL auto_increment,
 `type` enum('Q','A') NOT NULL,
 `pid` int(10) unsigned NOT NULL,
 `answer` smallint(5) unsigned NOT NULL,
 `author` int(10) unsigned NOT NULL,
 `point` int(10) NOT NULL,
 `count` int(10) unsigned NOT NULL,
 `hits` int(10) unsigned NOT NULL,  
 `status` tinyint(1) unsigned NOT NULL,
 `create` int(10) unsigned NOT NULL,
 `update` int(10) unsigned NOT NULL,
 `title` varchar(255) NOT NULL, 
 `alias` varchar(255) NOT NULL,
 `content` text,
 `tags` varchar(255) NOT NULL,
 PRIMARY KEY (`id`),
 KEY `alias` (`alias`),
 KEY `title` (`title`),
 KEY `status` (`status`),
 KEY `create` (`create`),
 KEY `hits` (`hits`),
 KEY `point` (`point`),
 KEY `author` (`author`),
 KEY `question_list` (`status`, `type`),
 KEY `answer_list` (`status`, `pid`, `type`),
 KEY `submit_list` (`status`, `type`, `title`),
 KEY `author_list` (`status`, `type`, `author`),
 KEY `order_id` (`create`, `id`),
 KEY `order_point` (`point`, `id`),
 KEY `order_hits` (`hits`, `id`),
 KEY `order_answer` (`answer`, `id`)
);